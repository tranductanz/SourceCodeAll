function Product(id, name, price, screen, backCamera, frontCamera, img, desc, type, image, inventory, rating){
     this.id = id;
     this.name = name;
     this.price = price;
     this.screen = screen;
     this.backCamera = backCamera;
     this.frontCamera = frontCamera;
     this.img = img;
     this.desc = desc;
     this.type = type;
     this.image = image;
     this.inventory = inventory;
     this.rating = rating;
}